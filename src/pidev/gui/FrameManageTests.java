    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
             String domTxt = tf_domaineAdd.getText();
       
       if (!domTxt.isEmpty()){
       
       IDomaineDAO ddao = DomaineDAO.getInstance();
       Domaine D = new Domaine(domTxt);
       
       ddao.insertDomaine(D);
       }
       else
            JOptionPane.showMessageDialog(this, "Veillez remplir les champs SVP", "Attention", JOptionPane.ERROR_MESSAGE);
       
       
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
      String repTxt = tf_reponse.getText();
        
        if (!repTxt.isEmpty()){
        IReponseDAO rdao = ReponseDAO.getInstance();
        
        int estcorrect=0;
        if(chk_correct.isSelected()){
            estcorrect=1;
        }
        else{
            estcorrect=0;
        }
        
        Question q = (Question) cbo_question.getSelectedItem();
       
        
        Reponse rep = new Reponse(q, repTxt, estcorrect);
        rdao.insertReponse(rep);
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Veillez remplir les champs SVP", "Attention", JOptionPane.ERROR_MESSAGE);
       
            
        }
    }//GEN-LAST:event_jButton2ActionPerformed

