    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
      String label = tf_question.getText();
        if (!label.isEmpty()){
       Domaine d = (Domaine)cbo_domaine.getSelectedItem();
        
       
        IQuestionDAO qdao = QuestionDAO.getInstance();
        Question q = new Question(d, label);
        qdao.insertQuestion(q);
        }
        else
            JOptionPane.showMessageDialog(this, "Veillez remplir les champs SVP", "Attention", JOptionPane.ERROR_MESSAGE);
       
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
         String ssd = tf_sousDomaine.getText();
        if (!ssd.isEmpty()){
            
            Domaine dom2 =(Domaine) cbo_domainesZ.getSelectedItem();
            
            SousDomaine sd = new SousDomaine(dom2, ssd);
            ISousDomaineDAO ssdao = SousDomaineDAO.getInstance();
            ssdao.insertSousDomaine(sd);
            
            
        }
        else{
            JOptionPane.showMessageDialog(this, "Veillez remplir les champs SVP", "Attention", JOptionPane.ERROR_MESSAGE);
       
       
        }
    }//GEN-LAST:event_jButton3ActionPerformed

